export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('en');

  const t = (key: string): string => {
    // First try current language
    const translation = translations[language]?.[key];
    if (translation) {
      return translation;
    }

    // Fallback to English
    const englishTranslation = translations['en']?.[key];
    if (englishTranslation) {
      console.warn(`Translation missing for key: ${key} in language: ${language}, using English fallback`);
      return englishTranslation;
    }

    // If no translation exists at all, return the key
    console.warn(`Translation missing for key: ${key} in all languages`);
    return key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
