export const getRandomCurricula = (count: number = 5) => {
  const shuffled = [...comprehensiveCurricula].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getCurriculaBySubject = (subject: string) => {
  return comprehensiveCurricula.filter(curr => 
    curr.subject.toLowerCase().includes(subject.toLowerCase())
  );
};
